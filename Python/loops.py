#For Loop With Tuples
fruits = ['apples', 'oranges', 'banana', 'kiwi', 'strawberry', 'mango', 'coconut', 'grapes']
for i,fruit in enumerate(fruits):
    print('Fruit: ', i+1, fruit)
#For Loop
for _ in range(5):
    print("Heyo")

#While Loop   
y=0
sum =0
while y<10:
    sum = sum+y
    y +=1
print(sum)

#Counter
counter = 0

while counter<10:
    print(counter+1)
    counter += 1